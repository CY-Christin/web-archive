// Rebuild the full-text index from R2 for existing pages, in bounded batches.
// Call repeatedly, passing back the returned `cursor`, until `done` is true.
app.post(
  '/reindex',
  validator('json', (value, c) => {
    const schema = z.object({
      cursor: z.number().int().nonnegative().default(0),
      limit: z.number().int().positive().max(50).default(10),
    })
    const parsed = schema.safeParse(value ?? {})
    if (!parsed.success) {
      return c.json(result.error(400, parsed.error.errors[0]?.message ?? 'Invalid request'))
    }
    return parsed.data
  }),
  async (c) => {
    const { cursor, limit } = c.req.valid('json')
    const pages = await queryPagesForReindex(c.env.DB, { cursor, limit })

    let indexed = 0
    let failed = 0
    let lastId = cursor
    for (const page of pages) {
      lastId = page.id
      try {
        const object = await c.env.BUCKET.get(page.contentUrl)
        const html = object ? await object.text() : ''
        await upsertPageFts(c.env.DB, {
          pageId: page.id,
          title: page.title,
          pageDesc: page.pageDesc,
          content: htmlToText(html),
        })
        indexed++
      }
      catch (error) {
        console.error(`Failed to reindex page ${page.id}`, error)
        failed++
      }
    }

    return c.json(result.success({
      indexed,
      failed,
      cursor: lastId,
      done: pages.length < limit,
    }))
  },
)

const LINK_CHECK_TIMEOUT_MS = 8000
// Each probe is 1-2 outbound fetches; with the batch capped at 15 pages this
// stays far below the Worker's 50-subrequest budget.
const LINK_CHECK_CONCURRENCY = 5
const LINK_CHECK_MAX_LIMIT = 15

type ProbeResult = { status: LinkStatus, reason: 'cf-blocked' | null }

// A 403/503 carrying Cloudflare markers is CF challenging/blocking the probe,
// which says nothing about whether the page is alive for a real browser.
function isCloudflareBlocked(headers: Headers) {
  return headers.has('cf-mitigated')
    || headers.has('cf-ray')
    || (headers.get('server') ?? '').toLowerCase().includes('cloudflare')
}

// Conservative mapping — only clear signals produce a verdict, everything
// inconclusive is 'unknown' rather than a false 'dead':
//   2xx -> live; 3xx -> redirect; 404/410 -> dead;
//   403/503 behind Cloudflare -> unknown + 'cf-blocked'; other 4xx/5xx -> unknown.
function classifyLinkStatus(res: Response): ProbeResult {
  const { status } = res
  if (status >= 200 && status < 300)
    return { status: 'live', reason: null }
  if (status >= 300 && status < 400)
    return { status: 'redirect', reason: null }
  if (status === 404 || status === 410)
    return { status: 'dead', reason: null }
  if ((status === 403 || status === 503) && isCloudflareBlocked(res.headers))
    return { status: 'unknown', reason: 'cf-blocked' }
  return { status: 'unknown', reason: null }
}

// HEAD first (cheap); fall back to GET when the server rejects HEAD (405/501)
// or the HEAD attempt itself fails. Redirects are reported, not followed.
async function probeLink(pageUrl: string): Promise<ProbeResult> {
  // Only probe web URLs; malformed or non-http(s) strings are 'dead' without
  // spending any fetches.
  try {
    const { protocol } = new URL(pageUrl)
    if (protocol !== 'http:' && protocol !== 'https:')
      return { status: 'dead', reason: null }
  }
  catch {
    return { status: 'dead', reason: null }
  }
  const attempt = (method: 'HEAD' | 'GET') => fetch(pageUrl, {
    method,
    redirect: 'manual',
    signal: AbortSignal.timeout(LINK_CHECK_TIMEOUT_MS),
  })
  try {
    const res = await attempt('HEAD')
    if (res.status !== 405 && res.status !== 501)
      return classifyLinkStatus(res)
  }
  catch {
    // network error / timeout: retry once with GET below
  }
  try {
    const res = await attempt('GET')
    return classifyLinkStatus(res)
  }
  catch {
    // Both attempts threw (timeout / network error): no conclusion, not a dead verdict.
    return { status: 'unknown', reason: null }
  }
}

// Bounded worker pool; `next++` is race-free because workers only interleave at awaits.
async function mapWithConcurrency<T, R>(items: T[], limit: number, fn: (item: T) => Promise<R>): Promise<R[]> {
  const results: R[] = Array.from({ length: items.length })
  let next = 0
  const workers = Array.from({ length: Math.min(limit, items.length) }, async () => {
    while (next < items.length) {
      const index = next++
      results[index] = await fn(items[index])
    }
  })
  await Promise.all(workers)
  return results
}

// Manual link-health check over the archive, in bounded batches. Call
// repeatedly, passing back the returned `cursor`, until `done` is true
// (same cursor-loop protocol as /reindex).
app.post(
  '/recheck',
  validator('json', (value, c) => {
    const schema = z.object({
      folderId: z.number().int().optional(),
      cursor: z.number().int().nonnegative().default(0),
      limit: z.number().int().positive().max(LINK_CHECK_MAX_LIMIT).default(10),
    })
    const parsed = schema.safeParse(value ?? {})
    if (!parsed.success) {
      return c.json(result.error(400, parsed.error.errors[0]?.message ?? 'Invalid request'))
    }
    return parsed.data
  }),
  async (c) => {
    const { folderId, cursor, limit } = c.req.valid('json')
    const pages = await queryPagesForRecheck(c.env.DB, { cursor, limit, folderId })

    const checked = await mapWithConcurrency(pages, LINK_CHECK_CONCURRENCY, async (page) => {
      const probe = await probeLink(page.pageUrl)
      return { id: page.id, linkStatus: probe.status, linkStatusReason: probe.reason }
    })
    const statuses = await updatePagesLinkStatus(c.env.DB, checked)

    return c.json(result.success({
      checked: pages.length,
      cursor: pages.length > 0 ? pages[pages.length - 1].id : cursor,
      done: pages.length < limit,
      statuses,
    }))
  },
)

