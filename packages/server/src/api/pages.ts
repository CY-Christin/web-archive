// One AI call per page, so batches stay much smaller than recheck's.
const AI_CLASSIFY_MAX_LIMIT = 5

// Batch AI folder classification ("AI 整理") over ONE folder: each of its pages
// is sorted into another existing folder — or a newly created one when the model
// says none fits — in bounded batches. folderId is required by design: the user
// runs this on a selected (e.g. uncategorized) folder, and already-sorted folders
// must stay untouched. The model may answer "no fitting destination", which
// leaves the page in place. Call repeatedly, passing back the returned `cursor`,
// until `done` is true (same cursor-loop protocol as /recheck).
app.post(
  '/ai_classify_folder',
  validator('json', (value, c) => {
    const schema = z.object({
      folderId: z.number({ message: 'Folder ID is required' }).int().nonnegative(),
      cursor: z.number().int().nonnegative().default(0),
      limit: z.number().int().positive().max(AI_CLASSIFY_MAX_LIMIT).default(3),
    })
    const parsed = schema.safeParse(value ?? {})
    if (!parsed.success) {
      return c.json(result.error(400, parsed.error.errors[0]?.message ?? 'Invalid request'))
    }
    return parsed.data
  }),
  async (c) => {
    const { folderId, cursor, limit } = c.req.valid('json')

    // Same config guards as autoEnrichPage, but surfaced as an error so the
    // frontend can toast once and stop the loop.
    let config
    try {
      config = await getAITagConfig(c.env.DB)
    }
    catch {
      return c.json(result.error(500, 'AI not configured'))
    }
    if (
      config.enabled === false
      || !config.model
      || (config.type === 'openai' && (!config.baseUrl || !config.apiKey))
      || (config.type === 'cloudflare-gateway' && (!config.baseUrl || !config.gatewayToken))
    ) {
      return c.json(result.error(500, 'AI not configured'))
    }

    const pages = await queryPagesForClassify(c.env.DB, { folderId, cursor, limit })
    const folders = await selectAllFolders(c.env.DB)
    const sourceFolder = folders.find(folder => folder.id === folderId)
    // Candidate destinations exclude the source folder itself (organizing it means
    // moving pages OUT of it). Case-insensitive lookup, extended as new folders are
    // created within the batch so later pages reuse them without a second creation.
    const folderByName = new Map(folders.filter(folder => folder.id !== folderId).map(folder => [folder.name.toLowerCase(), { id: folder.id, name: folder.name }]))

    let classified = 0
    let moved = 0
    let createdFolders = 0
    const results: Array<{ id: number, folderId: number, folderName: string, createdFolder: boolean }> = []
    // Sequential on purpose: one AI call per page, and D1 writes shouldn't race.
    for (const page of pages) {
      try {
        const answer = await classifyFolder(c.env, config, { title: page.title, pageDesc: page.pageDesc }, Array.from(folderByName.values(), folder => folder.name))
        // No parseable answer, the model said nothing fits, or it echoed the source
        // folder back (possibly as a "new" one): leave the page in place.
        if (!answer || answer.folder === null)
          continue
        if (sourceFolder && answer.folder.toLowerCase() === sourceFolder.name.toLowerCase())
          continue
        let target = folderByName.get(answer.folder.toLowerCase())
        let createdFolder = false
        if (!target) {
          // Unknown name without an isNew claim is an unusable answer: leave the page in place.
          if (!answer.isNew)
            continue
          const folderId = await insertFolder(c.env.DB, answer.folder)
          target = { id: folderId, name: answer.folder }
          folderByName.set(answer.folder.toLowerCase(), target)
          createdFolder = true
          createdFolders++
        }
        classified++
        if (target.id !== page.folderId) {
          await updatePageFolderId(c.env.DB, page.id, target.id)
          moved++
        }
        results.push({ id: page.id, folderId: target.id, folderName: target.name, createdFolder })
      }
      catch (error) {
        // Per-page best-effort: a failed classification skips the page, the batch goes on.
        console.error(`Failed to classify folder for page ${page.id}`, error)
      }
    }

    return c.json(result.success({
      classified,
      moved,
      createdFolders,
      cursor: pages.length > 0 ? pages[pages.length - 1].id : cursor,
      done: pages.length < limit,
      results,
    }))
  },
)

